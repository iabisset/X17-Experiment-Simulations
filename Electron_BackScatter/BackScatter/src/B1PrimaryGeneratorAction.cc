void B1PrimaryGeneratorAction::GeneratePrimaries(G4Event* anEvent)
{
  // This function is called at the beginning of each event.

  events += 1;
  if (events % 10000 == 0 && events < 930000) {
    cout << events << endl;
  }
  else if (events >= 930000) {
    cout << events << endl;
  }
  G4double envSizeXY = 0;
  G4double envSizeZ = 0;

  if (!fEnvelopeBox)
  {
    G4LogicalVolume* envLV
      = G4LogicalVolumeStore::GetInstance()->GetVolume("Envelope");
    if ( envLV ) fEnvelopeBox = dynamic_cast<G4Box*>(envLV->GetSolid());
  }

  // check for errors in basic envelope setup.
  if ( fEnvelopeBox ) {
    envSizeXY = fEnvelopeBox->GetXHalfLength()*2.;
    envSizeZ = fEnvelopeBox->GetZHalfLength()*2.;
  }
  else  {
    G4ExceptionDescription msg;
    msg << "Envelope volume of box shape not found.\n";
    msg << "Perhaps you have changed geometry.\n";
    msg << "The gun will be place at the center.";
    G4Exception("B1PrimaryGeneratorAction::GeneratePrimaries()",
     "MyCode0002",JustWarning,msg);
  }


  // Set up coordinates for a random direction within a cone of 25 degrees
  // oriented towards the detector.
  G4double theta = 0.2181662/2;
  G4double thecos = cos(theta);
  G4double yCoord = (1 - thecos)*G4UniformRand() + thecos;

  G4double randang = (2*M_PI) * ( G4UniformRand() );
  fParticleGun->SetParticleMomentumDirection(G4ThreeVector(sqrt(1-yCoord*yCoord)*sin(randang),yCoord,sqrt(1-yCoord*yCoord)*cos(randang)));

  // This simulation uses 5 MeV electrons.
  energy=5*MeV;
  fParticleGun->SetParticleEnergy(energy);

  fParticleGun->GeneratePrimaryVertex(anEvent);

}

